export async function POST(request: NextRequest) {
  try {
    const { message } = await request.json();

    if (!message || typeof message !== "string") {
      return NextResponse.json(
        { error: "Сообщение обязательно" },
        { status: 400 }
      );
    }

    // Safety check
    const safety = checkSafety(message);
    if (!safety.safe) {
      return NextResponse.json({
        reply: "Извините, я специализируюсь только на вопросах строительства быстровозводимых зданий. Задайте вопрос о наших услугах — с удовольствием помогу!",
        blocked: true,
      });
    }

    // Проверяем наличие API ключа
    const apiKey = process.env.DEEPSEEK_API_KEY || process.env.OPENAI_API_KEY || "sk-2xzqcmMtDZn1gt_w8zwZNw";
    if (!apiKey) {
      // Fallback: имитируем умный ответ если нет ключа
      return NextResponse.json({
        reply: generateFallbackResponse(message),
        fallback: true,
      });
    }

    // Запрос к BlackRoute API (OpenAI-compatible)
    const response = await fetch("https://open.blackroute.space/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        model: "deepseek-chat",  // или "gpt-4" если доступно
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          { role: "user", content: message },
        ],
        temperature: 0.7,
        max_tokens: 1000,
      }),
    });

    if (!response.ok) {
      throw new Error(`API error: ${response.status}`);
    }

    const data = await response.json();
    const reply = data.choices[0]?.message?.content || "Извините, не удалось получить ответ.";

    return NextResponse.json({ reply });

  } catch (error) {
    console.error("Chat API error:", error);
    return NextResponse.json({
      reply: "Извините, произошла техническая ошибка. Пожалуйста, позвоните нам: +7 (800) 100-91-51",
      error: true,
    }, { status: 200 });
  }
}

